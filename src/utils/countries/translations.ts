/**
 * Returns the localized name of a country given its English name and a language code.
 * Falls back to the original English name if no translation is available.
 */
export function localizeCountryName(name: string, lang: string): string {
    if (!name) return name;
    return countryTranslations[lang]?.[name] ?? name;
}
